// Math operations example
console.log("PI: " + Math.PI);
console.log("E: " + Math.E);

console.log("abs(-5): " + Math.abs(-5));
console.log("floor(3.7): " + Math.floor(3.7));
console.log("ceil(3.2): " + Math.ceil(3.2));
console.log("round(3.5): " + Math.round(3.5));

console.log("min(5, 3, 8, 1): " + Math.min(5, 3, 8, 1));
console.log("max(5, 3, 8, 1): " + Math.max(5, 3, 8, 1));

console.log("pow(2, 8): " + Math.pow(2, 8));
console.log("sqrt(16): " + Math.sqrt(16));
